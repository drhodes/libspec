import os
import tempfile
import difflib
from pathlib import Path
from lxml import etree
from xmldiff import main

def get_latest_xml_files(directory):
    path = Path(directory)
    xml_files = list(path.glob('*.xml'))
    if len(xml_files) == 0:
        return None
    
    file_info = []
    import datetime
    for f in xml_files:
        try:
            tree = etree.parse(str(f))
            root = tree.getroot()
            date_str = root.get('date-created')
            if date_str:
                file_info.append((date_str, f))
            else:
                # Fallback to file mtime formatted as ISO string
                mtime = os.path.getmtime(str(f))
                dt = datetime.datetime.fromtimestamp(mtime).astimezone()
                file_info.append((dt.isoformat(), f))
        except Exception:
            mtime = os.path.getmtime(str(f))
            dt = datetime.datetime.fromtimestamp(mtime).astimezone()
            file_info.append((dt.isoformat(), f))
    
    # Sort by the timestamp (date-created string or mtime)
    file_info.sort(key=lambda x: x[0])
    
    if len(file_info) == 1:
        # Only one spec exists — bootstrap case; diff against a null spec
        return None, file_info[-1][1]
    
    return file_info[-2][1], file_info[-1][1]

NULL_SPEC_XML = b"""<?xml version='1.0' encoding='utf-8'?>
<specification_set date-created="" />"""


def _node_text(spec, tag):
    node = spec.find(tag)
    if node is None or node.text is None:
        return ""
    return node.text.strip()


def _summarize_text(text, limit=120):
    cleaned = " ".join((text or "").split())
    if len(cleaned) <= limit:
        return cleaned
    return cleaned[: limit - 3] + "..."


def _patch_block(label, old_text, new_text):
    old_lines = (old_text or "").splitlines()
    new_lines = (new_text or "").splitlines()
    diff_lines = list(
        difflib.unified_diff(
            old_lines,
            new_lines,
            fromfile=f"old/{label}",
            tofile=f"new/{label}",
            lineterm="",
        )
    )
    if not diff_lines:
        return f"{label}: <no textual changes>"
    return f"{label}:\n" + "\n".join(diff_lines)


def _spec_ref(spec):
    return spec.get("ref") or spec.get("type")


def _specs_by_ref(root):
    specs = {}
    for spec in root.xpath("//specification"):
        ref = _spec_ref(spec)
        if ref:
            specs[ref] = spec
    return specs


def _libspec_version(root):
    return root.get("libspec-version") or ""


def _major_version(version):
    major = version.split(".", 1)[0].strip()
    return int(major) if major.isdigit() else None


def _major_versions_compatible(old_root, new_root):
    old_major = _major_version(_libspec_version(old_root))
    new_major = _major_version(_libspec_version(new_root))
    return old_major is None or new_major is None or old_major == new_major


def _version_mismatch_message(old_file, new_file, old_root, new_root):
    return (
        "Error: refusing to diff specs generated by different libspec major versions "
        f"({old_file.name}: {_libspec_version(old_root) or '<missing>'}, "
        f"{new_file.name}: {_libspec_version(new_root) or '<missing>'}).\n"
        "Workaround: regenerate both XML specs with the same libspec major version, "
        "usually by rebuilding the older project commit with the current libspec, "
        "then rebuilding the current commit and diffing those generated files.\n"
        "See docs/cross-major-version-diffs.org for the full workflow and edge cases."
    )


def get_specs_for_diff(dir_arg):
    """Return (old_file, new_file, old_tree, new_root) for diffing."""
    files = get_latest_xml_files(dir_arg)
    if not files:
        return None, None, None, None

    old_file, new_file = files

    if old_file is None:
        try:
            new_tree = etree.parse(str(new_file))
            new_root = new_tree.getroot()
            return None, new_file, None, new_root
        except Exception as e:
            print(f"Error parsing XML: {e}")
            return None, None, None, None
    else:
        try:
            new_tree = etree.parse(str(new_file))
            new_root = new_tree.getroot()
            return old_file, new_file, None, new_root
        except Exception as e:
            print(f"Error parsing XML: {e}")
            return None, None, None, None


def generate_patch(dir_arg):
    """Generate a structured diff between the two latest XML specs."""
    old_file, new_file, _, new_root = get_specs_for_diff(dir_arg)
    if new_file is None:
        print("Error: No XML spec files found in the directory.")
        return

    try:
        old_root = etree.fromstring(NULL_SPEC_XML) if old_file is None else etree.parse(str(old_file)).getroot()
        if not _major_versions_compatible(old_root, new_root):
            print(_version_mismatch_message(old_file, new_file, old_root, new_root))
            return
    except Exception as e:
        print(f"Error parsing XML: {e}")
        return

    if old_file is None:
        print(f"Diffing State: <null spec> -> {new_file.name}")
    else:
        print(f"Diffing State: {old_file.name} -> {new_file.name}")
    print("=" * 60)

    diffs = _xml_diffs(old_file, new_file)
    if not diffs:
        print("No changes detected.")
        return

    old_specs = {s.get('type'): s for s in old_root.xpath('//specification')}
    new_specs = {s.get('type'): s for s in new_root.xpath('//specification')}
    new_specs_by_ref = _specs_by_ref(new_root)

    all_components = sorted(set(old_specs.keys()) | set(new_specs.keys()))
    
    # 1. Identify changed components and collect their changes
    diff_entries = []
    for comp_type in all_components:
        old_spec = old_specs.get(comp_type)
        new_spec = new_specs.get(comp_type)
        if old_spec is None:
            diff_entries.append(('NEW', comp_type, old_spec, new_spec, []))
        elif new_spec is None:
            diff_entries.append(('REMOVED', comp_type, old_spec, new_spec, []))
        else:
            changes = _compare_specs(old_spec, new_spec)
            if changes:
                diff_entries.append(('CHANGED', comp_type, old_spec, new_spec, changes))

    # 4. Print individual diffs
    for entry in diff_entries:
        action, comp_type, old_spec, new_spec, changes = entry
        if action == 'NEW':
            if _has_display_content(new_spec):
                print(f"\n[NEW] {comp_type}")
                _print_spec(new_spec, new_specs_by_ref)
        elif action == 'REMOVED':
            print(f"\n[REMOVED] {comp_type}")
        elif action == 'CHANGED':
            print(f"\n[CHANGED] {comp_type}")
            for change in changes:
                print(f"  - {change}")
            _print_inherited_context(new_spec, new_specs_by_ref)


def _xml_diffs(old_file, new_file):
    if old_file is not None:
        return main.diff_files(str(old_file), str(new_file))

    with tempfile.NamedTemporaryFile(suffix='.xml', mode='wb', delete=False) as tmp:
        tmp.write(NULL_SPEC_XML)
        tmp_path = tmp.name
    try:
        return main.diff_files(tmp_path, str(new_file))
    finally:
        os.unlink(tmp_path)


class SpecField:
    """Base class for specification fields that can be displayed or compared."""
    TAGS = []

    def __init__(self, spec):
        self.spec = spec

    def get_value(self):
        """Extract value from top-level or delta_requirements."""
        for tag in self.TAGS:
            val = _node_text(self.spec, tag)
            if not val:
                val = _node_text(self.spec, f"delta_requirements/{tag}")
            if val:
                return val
        return ""

    def display(self, seen_values=None):
        pass

    def diff(self, old_spec):
        """Compare with old_spec and return a change string or list of strings."""
        return None


class Docstring(SpecField):
    TAGS = ["docstring"]

    def display(self, seen_values=None):
        val = self.get_value()
        if val:
            lines = val.splitlines()
            print(f"  docstring: {lines[0]}")
            for line in lines[1:]:
                print(f"    {line}")
            if seen_values is not None:
                seen_values.add(val)

    def diff(self, old_spec):
        old_val = _node_text(old_spec, 'docstring')
        new_val = _node_text(self.spec, 'docstring')
        if old_val != new_val:
            return _patch_block("docstring", old_val, new_val)


class Title(SpecField):
    TAGS = ["title"]

    def display(self, seen_values=None):
        val = self.get_value()
        if val:
            print(f"  title: {val}")
            if seen_values is not None:
                seen_values.add(val)

    def diff(self, old_spec):
        old_val = _node_text(old_spec, 'title') or _node_text(old_spec, 'delta_requirements/title')
        new_val = self.get_value()
        if old_val != new_val:
            return f"title: {old_val or '<missing>'} -> {new_val or '<missing>'}"


