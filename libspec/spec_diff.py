class DeltaRequirements(SpecField):
    HANDLED_TAGS = {"docstring", "docstring_template", "title", "req_id", "description", "notes", "inherits", "effective_req_ids", "overrides"}

    def display(self, seen_values=None):
        deltas = self.spec.xpath('delta_requirements/*')
        if not deltas:
            return
            
        # Filter out fields already handled by Primary classes
        unique_deltas = []
        for d in deltas:
            val = (d.text or '').strip()
            if d.tag in self.HANDLED_TAGS:
                continue
            if seen_values is not None and val in seen_values:
                continue
            unique_deltas.append(d)
            
        if unique_deltas:
            print("  delta_requirements:")
            for d in unique_deltas:
                val = (d.text or '').strip()
                print(f"    {d.tag}: {val}")

    def diff(self, old_spec):
        old_docstring = _node_text(old_spec, 'docstring') or _node_text(old_spec, 'docstring_template')
        new_docstring = _node_text(self.spec, 'docstring') or _node_text(self.spec, 'docstring_template')
        docstring_changed = old_docstring != new_docstring

        old_deltas = {d.tag: d.text for d in old_spec.xpath('delta_requirements/*')}
        new_deltas = {d.tag: d.text for d in self.spec.xpath('delta_requirements/*')}
        
        changes = []
        if old_deltas != new_deltas:
            for tag in sorted(set(old_deltas.keys()) | set(new_deltas.keys())):
                old_val = old_deltas.get(tag, '<missing>')
                new_val = new_deltas.get(tag, '<missing>')
                if (
                    tag == "notes"
                    and docstring_changed
                    and old_val == old_docstring
                    and new_val == new_docstring
                ):
                    continue
                if old_val != new_val:
                    changes.append(f"delta.{tag}: {old_val} -> {new_val}")
        return changes


def _has_display_content(spec):
    """Check if a specification node has any content that would be displayed."""
    if _node_text(spec, 'docstring') or _node_text(spec, 'docstring_template'): return True
    if _node_text(spec, 'title'): return True
    if _node_text(spec, 'req_id'): return True
    if _node_text(spec, 'description'): return True
    if _node_text(spec, 'notes'): return True
    if spec.xpath('inherits/ref'): return True
    if spec.xpath('effective_req_ids/id'): return True
    if spec.xpath('overrides/field'): return True
    
    # Check for any delta_requirements that aren't standard tags
    deltas = spec.xpath('delta_requirements/*')
    handled = DeltaRequirements.HANDLED_TAGS
    if any(d.tag not in handled for d in deltas):
        return True
        
    return False


def _print_spec(spec, specs_by_ref=None):
    """Print the structured representation of a spec using polymorphic fields."""
    specs_by_ref = specs_by_ref or {}
    seen_values = set()

    fields = [
        Docstring(spec),
        Title(spec),
        ReqId(spec),
        Description(spec),
        Notes(spec),
        Inherits(spec, specs_by_ref),
        EffectiveReqIds(spec),
        Overrides(spec),
    ]

    for field in fields:
        field.display(seen_values)
        
    # DeltaRequirements handles everything else
    DeltaRequirements(spec).display(seen_values)


def _compare_specs(old_spec, new_spec, old_specs_by_ref=None, new_specs_by_ref=None, visited=None):
    """Compare two specs and return list of changes using polymorphic fields."""
    if visited is None:
        visited = set()
        
    spec_ref = _spec_ref(new_spec)
    if spec_ref in visited:
        return []
        
    current_visited = visited | {spec_ref} if spec_ref else visited
    
    changes = []
    
    # Define fields in the order they should appear in the diff output
    fields = [
        Title(new_spec),
        ReqId(new_spec),
        Inherits(new_spec, new_specs_by_ref, old_specs_by_ref, current_visited),
        EffectiveReqIds(new_spec),
        Overrides(new_spec),
        DeltaRequirements(new_spec),
        Docstring(new_spec),
        Description(new_spec),
        Notes(new_spec),
    ]

    for field in fields:
        diff_output = field.diff(old_spec)
        if diff_output:
            if isinstance(diff_output, list):
                changes.extend(diff_output)
            else:
                changes.append(diff_output)

    return changes


def _extract_context(node):
    """Recursively extract context from XML node."""
    ctx = {}
    if node is None:
        return ctx
    for child in node:
        if len(child) > 0:
            # Check if it's a list of items
            items = child.xpath('item')
            if items:
                ctx[child.tag] = [_extract_context_value(it) for it in items]
            else:
                ctx[child.tag] = _extract_context(child)
        else:
            ctx[child.tag] = (child.text or "").strip()
    return ctx


def _extract_context_value(node):
    """Extract value from a context node (either scalar or recursive)."""
    if len(node) == 0:
        return (node.text or "").strip()
    return _extract_context(node)


_STRUCTURAL_TAGS = {"context", "inherits", "source", "effective_req_ids", "overrides", "delta_requirements"}


def _build_child_context(spec):
    """Build template context from a spec node, merging context node, top-level fields, and delta_requirements."""
    # 1. Start with the explicit <context> node
    child_context = _extract_context(spec.find("context"))

    # 2. Merge top-level fields (like title, req_id) which templates often use
    for child in spec:
        if child.tag not in _STRUCTURAL_TAGS:
            val = (child.text or "").strip()
            if val and child.tag not in child_context:
                child_context[child.tag] = val

    # 3. Also merge from delta_requirements if present
    delta = spec.find("delta_requirements")
    if delta is not None:
        for child in delta:
            val = (child.text or "").strip()
            if val and child.tag not in child_context:
                child_context[child.tag] = val

    # 4. Fallback: feature_name defaults to the spec type (Feature.feature_name() returns __class__.__name__)
    if "feature_name" not in child_context:
        spec_type = spec.get("type")
        if spec_type:
            child_context["feature_name"] = spec_type

    return child_context


def _print_inherited_context(spec, specs_by_ref):
    inherits = [e.text for e in spec.xpath('inherits/ref')]
    if not inherits:
        return
    _print_inherited_specs(inherits, specs_by_ref, _build_child_context(spec))



def _print_inherited_specs(inherits, specs_by_ref, child_context=None):
    from jinja2 import Template
    inherited_specs, unresolved_refs = _inherited_specs(inherits, specs_by_ref)
    if inherited_specs:
        print("  inherited_specs (STRICTLY FOLLOW THE GUIDANCE BELOW):")
    for ref, spec_name, spec_node in inherited_specs:
        is_template = spec_node.get("template") == "true"
        if spec_node.get("template") is None:
            # Fallback for older XML specs
            req_text = _node_text(spec_node, "docstring") or _node_text(spec_node, "docstring_template") or ""
            is_template = "{{" in req_text or "{%" in req_text
        
        if is_template:
            print(f"    {spec_name}: {ref} (template instance)")
            requirement_text = _node_text(spec_node, "docstring") or _node_text(spec_node, "docstring_template")
            if requirement_text:
                # Always try to render if it's a template instance
                try:
                    requirement_text = Template(requirement_text).render(**(child_context or {})).strip()
                except Exception:
                    pass
                print("      requirement:")
                for line in requirement_text.splitlines():
                    print(f"        {line}")
        else:
            print(f"    {spec_name}: {ref}")
            requirement_text = _node_text(spec_node, "docstring") or _node_text(spec_node, "docstring_template")
            if requirement_text:
                print("      requirement:")
                for line in requirement_text.splitlines():
                    print(f"        {line}")

    if unresolved_refs:
        print("  unresolved_inherited_refs:")
        for ref in unresolved_refs:
            print(f"    {ref}")





def _inherited_specs(inherits, specs_by_ref):
    specs = []
    unresolved_refs = []
    seen_refs = set()

    def visit(ref):
        if not ref or ref in seen_refs:
            return
        seen_refs.add(ref)

        inherited_spec = specs_by_ref.get(ref)
        if inherited_spec is None:
            unresolved_refs.append(ref)
            return

        spec_name = inherited_spec.get("type") or ref.rsplit(".", 1)[-1]
        specs.append((ref, spec_name, inherited_spec))

        for child_ref in inherited_spec.xpath('inherits/ref'):
            visit(child_ref.text)

    for ref in inherits:
        visit(ref)

    return specs, unresolved_refs


